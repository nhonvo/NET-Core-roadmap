public class Employee
{
    private string Name;
    private double Salary;

    public string GetName()
    {
        return Name;
    }

    public void SetName(string name)
    {
        Name = name;
    }

    public double GetSalary()
    {
        return Salary;
    }

    public void SetSalary(double salary)
    {
        Salary = salary;
    }
}
